void ChangeClickability(bool& canclick, HWND hwnd)
{
    long style = GetWindowLong(hwnd, GWL_EXSTYLE);
    if (canclick)
    {
        style &= ~WS_EX_LAYERED;
        SetWindowLong(hwnd, GWL_EXSTYLE, style);
        SetForegroundWindow(hwnd);
    }
    else
    {
        style |= WS_EX_LAYERED;
        SetWindowLong(hwnd, GWL_EXSTYLE, style);
    }
}